def get_path(grid):
    path = []
    if len(grid) == 0:
        return
    path.append((0, 0))
    return move_from(grid, 0, 0, path)


def move_from(grid, row, col, path):
    print((col, row))
    if row == len(grid[0]) - 1:
        if col == len(grid) - 1:
            return (True, path)

    if row < len(grid[0]) -1 :
        new_row = row + 1
        if grid[col][new_row] != 0:
            new_path = path + [(col, new_row)]
            (down_is_last, down_path) = move_from(grid, new_row, col, new_path)
            if down_is_last:
                return True, down_path

    if col < len(grid) -1 :
        new_col = col + 1
        if grid[new_col][row] != 0:
            new_path = path + [(new_col, row)]
            (right_is_last, right_path) = move_from(grid, row, new_col, new_path)
            if right_is_last:
                return True, right_path

    return False, path


